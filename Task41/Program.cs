// Задача 41: Пользователь вводит с клавиатуры M чисел. Посчитайте, сколько чисел больше 0 ввёл пользователь.
// 0, 7, 8, -2, -2 -> 2
// -1, -7, 567, 89, 223-> 3



    {
        int count = 0; // Создаем переменную для хранения количества чисел больше 0
        Console.Write("Введите количество чисел: "); // Просим пользователя ввести количество чисел
        int m = int.Parse(Console.ReadLine()); // Считываем количество чисел
        for (int i = 0; i < m; i++) // Создаем цикл для ввода каждого числа
        {
            Console.Write("Введите число: ");
            int num = int.Parse(Console.ReadLine()); // Считываем число
            if (num > 0) // Если число больше 0, то увеличиваем счетчик
                count++;
        }
        Console.WriteLine($"Количество чисел больше 0: {count}"); // Выводим результат
    }
